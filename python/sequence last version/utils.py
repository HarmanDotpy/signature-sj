import random

def read_sequences(filename):
    sequences = {}
    with open(filename, "r") as input_file:
        name = ''
        for line in input_file:
            if not name:
                name = line
            else:
                sequences[name.rstrip()] = line.rstrip()
                name = ''
    return sequences

def read_sequences_with_additional_info(filename, multiplier, mutations):
    sequences = {}
    with open(filename, "r") as input_file:
        name = ''
        for line in input_file:
            if not name:
                name = line.rstrip()
            else:
                sec = line.rstrip()
                sequences[name] = sec
                for i in range(multiplier-1):
                    new_line = sec
                    for _ in range(mutations):
                        position = random.randint(0, len(new_line)-1)
                        new_line = new_line[:position]+'ACTG'[random.randint(0, 3)]+ new_line[position+1:]
                    sequences[name+str(i)] = new_line
                name = ''
    insert_noise(sequences)
    return sequences


def insert_noise(sequences):
    iteration = 0
    inserted_kmers = 6
    for name in sequences:
        if iteration < len(sequences) / inserted_kmers:
            sequences[name] = 'TTTT' + sequences[name][4:]
        elif iteration < 2 * len(sequences) / inserted_kmers:
            sequences[name] = 'CCCC' + sequences[name][4:]
        elif iteration < 3 * len(sequences) / inserted_kmers:
            sequences[name] = 'GGGG' + sequences[name][4:]
        elif iteration < 4 * len(sequences) / inserted_kmers:
            sequences[name] = 'ACAG' + sequences[name][4:]
        elif iteration < 5 * len(sequences) / inserted_kmers:
            sequences[name] = 'TAAC' + sequences[name][4:]
        iteration += 1

